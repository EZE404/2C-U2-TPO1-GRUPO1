    private void armarCabeceraTabla() {
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("ID");
        columnas.add("Nombre");
        columnas.add("Nota");

        for (Object it : columnas) {
            modelo.addColumn(it);
        }
        jt_materias.setModel(modelo);
    }

    private void borrarFilasTabla() {
        int a = modelo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }
    
    private void cargarTablaMaterias(Alumno alumno) {
        List<Inscripcion> inscripciones = id.inscripcionesAlumno(alumno);
        String si = "SÃ­";
        String no = "No";
        this.borrarFilasTabla();
        for (Inscripcion a: inscripciones) {
            modelo.addRow(new Object[]{a.getMateria().getId_materia(),a.getMateria().getNombre_materia(),a.getCalificacion()});  
        }
    }
