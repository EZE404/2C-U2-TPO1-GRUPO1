    private void cargarAlumnos() {

        ad.ver_alumnos().forEach((alumno) -> {
            jcb_form_ins_lista_alumnos.addItem(alumno);
        });

    }
    
    private void armaCabeceraTabla() {
        //Titulos de Columnas
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("ID");
        columnas.add("Nombre");
        columnas.forEach((it) -> {
            modelo_tabla.addColumn(it);
        });
        jt_form_ins_tabla_materias.setModel(modelo_tabla);
    }
    
    private void borraFilasTabla() {
        int a = modelo_tabla.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            modelo_tabla.removeRow(i);
        }
    }
    
    public void cargaDatosInscriptas() {
        borraFilasTabla();
        //Llenar filas
        Alumno seleccionado = (Alumno) jcb_form_ins_lista_alumnos.getSelectedItem();
        List<Materia> lista = id.materias_alumno(seleccionado.getId_alumno());
        lista.forEach((m) -> {
            modelo_tabla.addRow(new Object[]{m.getId_materia(), m.getNombre_materia()});
        });
    }
    
    public void actualizarBarraEstado(String mensaje) {
        jl_form_ins_barra_estado.setText(mensaje);
    }
